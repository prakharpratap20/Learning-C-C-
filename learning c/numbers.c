#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{   
    int num1 = 5;
    int num2 = 6;
    printf("%d\n", num1 * num2);
    printf("%f\n", 5.0 + 4.5);
    printf("%f\n", 5.0 * 4.5);
    printf("%f\n", 5.0 - 4.5);
    printf("%f\n", 5.0 / 4.5);
    printf("%d\n", 5 / 4);
    printf("%f\n", pow(5, 3));
    printf("%f\n", sqrt(25));
    printf("%f\n", ceil(25.562));
    printf("%f\n", floor(25.562));

    return 0;
}